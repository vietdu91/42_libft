#include "libft.h"

void	*ft_memset(void *s, int a, size_t c)
{
	size_t	b;
	char	*ch;

	ch = s;
	b = 0;
	while (b < c)
	{
		ch[b] = (unsigned char)a;
		b++;
	}
	return (s);
}
